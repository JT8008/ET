
SELECT
  s.CLIENT_NUMBER,
  s.PRODUCT_CODE,
  c.COMPANY_NAME,
  s.COMPANY_CODE
    
FROM
   sales s
   JOIN company c ON c.COMPANY_CODE = s.COMPANY_CODE
 
WHERE  
   s.PRODUCT_CODE= 10 OR s.PRODUCT_CODE = 5 OR s.PRODUCT_CODE = 2
   
GROUP BY

  s.CLIENT_NUMBER,
  s.PRODUCT_CODE,
  c.COMPANY_NAME,
  s.COMPANY_CODE
 
 

